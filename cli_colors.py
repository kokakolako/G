class fg():
    def black( text ):
        return "\033[30m" + text + "\033[0m"
    def red( text ):
        return "\033[31m" + text + "\033[0m"
    def green( text ):
        return "\033[32m" + text + "\033[0m"
    def yellow( text ):
        return "\033[33m" + text + "\033[0m"
    def blue( text ):
        return "\033[34m" + text + "\033[0m"
    def violett( text ):
        return "\033[34m" + text + "\033[0m"
    def cyan( text ):
        return "\033[35m" + text + "\033[0m"
    def white( text ):
        return "\033[36m" + text + "\033[0m"

